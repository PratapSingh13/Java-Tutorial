/*This Program is for Data type tutorial*/

public class DataTypeTutorial_1
{
	public static void main(String[] args) 
	{
		byte num = 2;
		System.out.println("num = " +num);
		
		int age = 24;
		System.out.println("Age = " +age);
		
		float rateOfInterest = 2.5f;
		double valueOfPie = 3.1428571;
		System.out.println("Rate of Interest = " +rateOfInterest);
		System.out.println("Value of Pie = " +valueOfPie);
		
		boolean isThisSeriesCool = true;
		System.out.println("Is This Series is Cool: " +isThisSeriesCool);
	}

}
